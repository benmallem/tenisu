package com.example.tenisu.models;

public class PlayerData {

    private int rank;
    private int points;
    private int weight;
    private int height;
    private int age;
    private int[] last;

    public int getRank() {
        return rank;
    }

    public int getPoints() { return points; }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }

    public int[] getLast() {
        return last;
    }

}
